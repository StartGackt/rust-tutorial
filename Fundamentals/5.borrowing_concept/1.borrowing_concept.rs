//
// Borrowing มี 2 ประเภท
//
// 1) Borrowing Immutable (&T)   // &book = ขอยืมไปดูนะ เดี๋ยวคืน
//    - ยืมข้อมูลมาใช้อ่านอย่างเดียว
//    - ขีดเขียน / แก้ไขไม่ได้
//    - เพื่อนหลายคนยืมดูพร้อมกันได้
//
// 2) Borrowing Mutable (&mut T) // &mut book = ขอยืมไปเขียนนะ
//    - อ่านและขีดเขียนได้ (แก้ไขค่าได้)
//    - ในช่วงเวลานั้น มีคนยืมได้แค่คนเดียว
//    - ระหว่างยืมเขียน ห้ามคนอื่นยืมดูหรือยืมเขียน
