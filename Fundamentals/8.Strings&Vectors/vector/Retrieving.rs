// Retrieving  การหยิบของออกมาดู
// ใส่ผลไม้ 3 ลูก (fruits = vec!["แอปเปิ้ล", "ส้ม", "กล้วย"]) ตอนนี้คุณอยากจะหยิบลูกที่ 2 ออกมาดูว่ามันคืออะไร นี่แหละคือการ Retrieving

fn main() {
    let fruits = vec!["แอปเปิ้ล", "ส้ม", "กล้วย"];
    println!("ลูกที่ 2 คือ: {}", fruits[1]);
}


// การ Retrieving มี 2 แบบ:
// 1. ดูทีละชิ้น (ตัวที่ 0, 1, 2, ...)
// 2. ดูทั้งหมดพร้อมกัน (ใช้ &fruits)