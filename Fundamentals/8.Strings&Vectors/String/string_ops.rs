// String Manipulation คือชุดคำสั่งที่บอกคอมพิวเตอร์ว่า ให้ทำอะไรกับตัวหนังสือบ้าง
// เรามีข้อความชุดหนึ่งอยู่ในมือ "สวัสดีครับ คุณผู้ชาย" การจัดการข้อความ (Manipulation) คือการทำสิ่งเหล่านี้

// การตัด (Cut/Slicing) 
  // อยากได้ "สวัสดี" ออกมา จาก "สวัสดีครับ คุณผู้ชาย" เหลือแค่ "สวัสดี" ออกมาใช้งาน
// การต่อ (Concatenation) 
  // เรามีชื่อ "สวัสดีครับ คุณผู้ชาย" เรามีอีกคำจะเพิ่ม "สุดหล่อ" เข้าไปเป็น "สวัสดีครับ คุณผู้ชาย สุดหล่อ" 


// สรุป  String Manipulation คือ "ศิลปะการปรับแต่งคำพูดและตัวหนังสือ" ให้อยู่ในรูปแบบที่เราต้องการโดยอัตโนมัติ



// EX 
fn main() {
    // ---------------------------------------------------------
    // 1. การต่อ (Concatenation)
    // โจทย์: มี "สวัสดีครับ คุณผู้ชาย" อยากเติม "สุดหล่อ" ต่อท้าย
    // ---------------------------------------------------------
    
    // สร้างสมุดจด (String) ขึ้นมาก่อน เพราะเราต้องเขียนเพิ่ม
    let mut greeting = String::from("สวัสดีครับ คุณผู้ชาย");
    
    // สั่งเขียนต่อ (Push)
    greeting.push_str(" สุดหล่อ"); 
    
    println!("ผลลัพธ์การต่อ: {}", greeting); 
    // ออกมาเป็น: สวัสดีครับ คุณผู้ชาย สุดหล่อ



    // ---------------------------------------------------------
    // 2. การตัด (Slicing)
    // โจทย์: มี "Rust Language" อยากตัดเอาแค่คำว่า "Rust" ออกมาใช้
    // ---------------------------------------------------------
    
    let text = "Rust Language";

    // สั่งตัด (Slice) โดยใช้ [0..4]
    // แปลว่า: เอาตั้งแต่ตัวที่ 0 จนถึง (แต่ไม่เอา) ตัวที่ 4 -> R, u, s, t
    let short_word = &text[0..4]; 
    
    println!("ผลลัพธ์การตัด: {}", short_word);
    // ออกมาเป็น: Rust
}
 



