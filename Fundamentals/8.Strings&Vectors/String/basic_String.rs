// Understanding String and str
// String กับ str ต่างกันยังไง 


// String = ข้อความ (Mutable)
   // String สมุดจดที่คุณเขียนเอง อยากเพิ่ม อยากลบ ทำได้หมด
    // เหมาะกับข้อความที่เปลี่ยนไปเรื่อย ๆ เช่น ข้อมูลที่ผู้ใช้พิมพ์
    // การใช้งานแบบนี้ต้องขอพื้นที่ความจำไว้ก่อน
    // เลยมีค่าใช้จ่ายในการใช้งานนิดหน่อย


// str = ป้ายข้อความ (Immutable)
    // str สมุดจดที่คุณอ่านได้ อยากเพิ่ม อยากลบ ทำไม่ได้
    // อ่านได้อย่างเดียว แต่แก้ไขไม่ได้
    // ตัว &str ไม่ได้เก็บข้อความเอง
   // แค่ชี้ไปดูข้อความที่มีอยู่แล้ว
  // ใช้งานเบา เร็ว ไม่ต้องขอพื้นที่เพิ่ม

  // สรุปสั้น ๆ
// อ่านอย่างเดียว → ใช้ str
// ต้องแก้ไข → ใช้ String



//* ---------------------------------------------------------

// อธิบาย HEAP กับ STACK 

// STACK:
// เปรียบเหมือน "โต๊ะทำงานเล็ก ๆ ข้างตัว"
// ใช้เก็บข้อมูลชิ้นเล็ก ๆ ที่รู้ขนาดแน่นอน
// หยิบเข้า วางออก ได้เร็วมาก
// ไม่ต้องขอพื้นที่ล่วงหน้า
// ใช้งานง่าย เร็ว และแทบไม่เสียค่าใช้จ่าย

// -------------------------------------------------

// HEAP:
// เปรียบเหมือน "โกดังเก็บของขนาดใหญ่"
// ใช้เก็บข้อมูลชิ้นใหญ่ หรือข้อมูลที่ขนาดเปลี่ยนได้
// ก่อนใช้งานต้องไปขอพื้นที่จากระบบก่อน
// การหยิบใช้งานช้ากว่า stack เล็กน้อย
// มีค่าใช้จ่ายในการจัดการพื้นที่

// -------------------------------------------------

// สรุปสั้น ๆ
// ของเล็ก รู้ขนาด → STACK (เร็ว)
// ของใหญ่ ขนาดเปลี่ยน → HEAP (ยืดหยุ่น)



// การจัดการ memory ของ String และ str

// String:
// ข้อความที่เปลี่ยนแปลงได้
// ตัว String เองเก็บข้อมูลบางอย่างไว้บน STACK
// (เช่น ความยาว และตำแหน่งที่ข้อมูลอยู่)
// ส่วนข้อความจริง ๆ ถูกเก็บไว้ใน HEAP
// เพราะขนาดข้อความอาจยาวขึ้นหรือสั้นลงได้
// เมื่อเลิกใช้ String พื้นที่ใน HEAP จะถูกคืนให้ระบบอัตโนมัติ

// -------------------------------------------------

// str (หรือ &str):
// ข้อความที่อ่านอย่างเดียว
// ตัว &str ไม่ได้เก็บข้อความเอง
// แค่เป็นเหมือน "ป้ายบอกทาง"
// ชี้ไปยังข้อความที่มีอยู่แล้ว
// ข้อความนั้นอาจอยู่ใน HEAP (มาจาก String)
// หรือเป็นข้อความตายตัวที่อยู่ในตัวโปรแกรม
// เลยไม่ต้องจองพื้นที่ใหม่ ใช้งานเบาและเร็ว

// -------------------------------------------------

// สรุปสั้น ๆ
// String → มีข้อความเป็นของตัวเอง (ต้องดูแลพื้นที่)
// &str   → แค่ชี้ไปอ่านข้อความของคนอื่น

