// คือความปลอดภัย: ช่วยป้องกันการกรอกข้อมูลมั่วๆ (เช่น สถานะออเดอร์มีแค่ รอส่ง กับ ส่งแล้ว จะมาพิมพ์ ส่งแย้วจ้า ไม่ได้)
// คือกล่องเก็บข้อมูล (เฉพาะ Rust) แต่ละตัวเลือก สามารถห่อข้อมูลที่เกี่ยวข้องติดตัวไปด้วยได้




