// Vector ต้องเก็บของชนิดเดียวกันเท่านั้น" เช่น เก็บตัวเลขก็ต้องตัวเลขหมด ห้ามมีตัวหนังสือปน
// Using Vectors with Enums คือเทคนิคการ สวมชุดยูนิฟอร์ม ให้ข้อมูล เพื่อให้ Vector ยอมรับของที่ต่างกันได้
// ข้างในต่างกัน แต่ข้างนอกใส่ชุดเหมือนกัน
// SpreadsheetCell คือ ชุดยูนิฟอร์ม ที่สามารถเก็บข้อมูลต่างกันได้

// สร้าง Enum ชื่อ SpreadsheetCell ขึ้นมา เพื่อบอกว่า ไม่ว่าข้างในจะเป็นอะไร ข้างนอกให้เรียกว่าเซลล์เหมือนกันนะ

#[derive(Debug)] // แปะป้ายให้อ่านค่าได้
enum SpreadsheetCell {
    Int(i32),      // แบบที่ 1: เก็บเลขจำนวนเต็ม
    Float(f64),    // แบบที่ 2: เก็บเลขทศนิยม
    Text(String),  // แบบที่ 3: เก็บข้อความ
}

// สร้าง Vector (ที่รับแต่ชุดยูนิฟอร์มนี้)
// ทีนี้ก็สร้าง Vector ที่เก็บ SpreadsheetCell 
fn main() {
    // สร้าง Vector ที่เก็บข้อมูลผสมกันได้!
    let row = vec![
        SpreadsheetCell::Int(3),                        // ข้อมูลตัวเลข
        SpreadsheetCell::Text(String::from("สมชาย")),   // ข้อมูลข้อความ
        SpreadsheetCell::Float(10.5),                   // ข้อมูลทศนิยม
    ];

    // ผลลัพธ์: Vector ยอมรับหมด เพราะมองว่าทุกตัวคือ "SpreadsheetCell" เหมือนกัน
    println!("ข้อมูลในแถว: {:?}", row);
}

// อยากเก็บ "ของหลายประเภท" ไว้ใน "Vector เดียว" ... ให้ใช้ Enum มาห่อของพวกนั้นไว้ก่อน

